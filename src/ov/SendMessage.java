package ov;

public class SendMessage {

	private String type ;
	
	private String chat_obj;
	
	private String chat_from;
	
	private String con_message;
	
	private String ima_path;

	public String getIma_path() {
		return ima_path;
	}

	public void setIma_path(String ima_path) {
		this.ima_path = ima_path;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getChat_obj() {
		return chat_obj;
	}

	public void setChat_obj(String chat_obj) {
		this.chat_obj = chat_obj;
	}

	public String getChat_from() {
		return chat_from;
	}

	public void setChat_from(String chat_from) {
		this.chat_from = chat_from;
	}

	public String getCon_message() {
		return con_message;
	}

	public void setCon_message(String con_message) {
		this.con_message = con_message;
	}
	
	
}
